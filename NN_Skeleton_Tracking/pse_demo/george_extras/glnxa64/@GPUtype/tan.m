% tan - Tangent of argument in radians
% 
% SYNTAX
% 
% R = tan(X)
% X - GPUsingle, GPUdouble
% R - GPUsingle, GPUdouble
% 
% 
% MODULE NAME
% NUMERICS
% 
% DESCRIPTION
% TAN(X) is the tangent of the elements of X.
% Compilation supported
% 
% EXAMPLE
% 
% X   =   rand(10,GPUsingle);
% R   =   tan(X)
% X   =   rand(10,GPUdouble);
% R   =   tan(X)
% 
% 
% MATLAB COMPATIBILITY
% Not implemented for complex X.
